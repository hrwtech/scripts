import os

_base = os.__file__.split('Frameworks')[0]
_target = 'site-packages'
_mb = 10 ** -6


def walk(path, res=0):
	for path, n, fls in os.walk(path):
		if _target in path:
			sze = sum([os.path.getsize(os.path.join(path, f))
			for f in fls]) * _mb
			print('{} ({} MB)'.format(path[len(_base):], sze))
			res += sze
	return res
	
size = walk(_base)
print('{}\nSum : {} MB'.format('-' * 79, size))

